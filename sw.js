const CACHE_NAME = 'uds-hub-v1';
const ASSETS_TO_CACHE = [
  '/',
  '/simple (2).html',
  '/manifest.json'
];

// Install Service Worker and cache essential layouts
self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      return cache.addAll(ASSETS_TO_CACHE);
    })
  );
  self.skipWaiting();
});

// Activate handler
self.addEventListener('activate', (event) => {
  event.waitUntil(self.clients.claim());
});

// Fetch interception for offline loading support
self.addEventListener('fetch', (event) => {
  event.respondWith(
    caches.match(event.request).then((cachedResponse) => {
      return cachedResponse || fetch(event.request);
    })
  );
});

// LISTEN FOR PUSH NOTIFICATIONS (Even when the app is completely closed)
self.addEventListener('push', (event) => {
  let data = { title: 'UDS Hub Update', body: 'You have a new notice or marketplace message waiting!' };
  
  if (event.data) {
    try {
      data = event.data.json();
    } catch (e) {
      data.body = event.data.text();
    }
  }

  const options = {
    body: data.body,
    icon: '/logo-192.png',
    badge: '/logo-192.png',
    vibrate: [100, 50, 100],
    data: {
      dateOfArrival: Date.now(),
      primaryKey: '1'
    },
    actions: [
      { action: 'explore', title: 'Open App', icon: '/logo-192.png' }
    ]
  };

  event.waitUntil(
    self.registration.showNotification(data.title, options)
  );
});

// Handle Notification Clicks
self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  event.waitUntil(
    clients.matchAll({ type: 'window' }).then((clientList) => {
      for (const client of clientList) {
        if (client.url === '/' && 'focus' in client) return client.focus();
      }
      if (clients.openWindow) return clients.openWindow('/');
    })
  );
});